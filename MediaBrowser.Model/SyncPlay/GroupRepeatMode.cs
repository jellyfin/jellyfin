namespace MediaBrowser.Model.SyncPlay
{
    /// <summary>
    /// Enum GroupRepeatMode.
    /// </summary>
    public enum GroupRepeatMode
    {
        /// <summary>
        /// Repeat one item only.
        /// </summary>
        RepeatOne = 0,

        /// <summary>
        /// Cycle the playlist.
        /// </summary>
        RepeatAll = 1,

        /// <summary>
        /// Do not repeat.
        /// </summary>
        RepeatNone = 2
    }
}
